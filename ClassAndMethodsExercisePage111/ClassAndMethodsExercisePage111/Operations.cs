using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassAndMethodsExercisePage111
{
    class Operations
    {
        public int multipliedByTwo(int a)
        {
            int multiply = a * 2;
            return multiply;
        }

        public int addSix(int b)
        {
            int add = b + 6;
            return add;
        }

        public int totalAfterDivideByTwo(int c)
        {
            int divide = c / 2;
            return divide;
        }

    }
}
